#include<bits/stdc++.h>
using namespace std;
int main()
{
    int day ;
    cin>>day;
    switch(day){
        case 1:
        cout<<"Monaday";
        break;
        case 2:
        cout<<"Tuesday";
        break;
        case 3:
        cout<<"wednesday";
        break;
        case 4:
        cout<<"Thrusday";
        break;
        case 5:
        cout<<"Friday";
        break;
        case 6:
        cout<<"saturday";
        break;
        case 7:
        cout<<"Sunday";
        break;
        default:
        cout<<"Invalid case";
    }
    return 0;
}